#!/usr/bin/env python3
''' This script contains a type-annotated function concat
that takes a string str1 and a string str2 as arguments
and returns a concatenated string '''


def concat(str1: str, str2: str) -> str:
    '''concat that takes a string str1 and a string str2 as arguments
    and returns a concatenated string
    '''
    return str1 + str2
